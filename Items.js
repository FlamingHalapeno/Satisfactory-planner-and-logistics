// Helper functions for searching and filtering items
const ItemUtils = {
  // Search items by name
  searchByName: (query) => {
    return SatisfactoryItems.filter(item => 
      item.name.toLowerCase().includes(query.toLowerCase())
    );
  },

  // Get items by category
  getByCategory: (category) => {
    return SatisfactoryItems.filter(item => 
      item.category.toLowerCase() === category.toLowerCase()
    );
  },

  // Get all categories
  getCategories: () => {
    return [...new Set(SatisfactoryItems.map(item => item.category))].sort();
  },

  // Get items with energy value
  getItemsWithEnergy: () => {
    return SatisfactoryItems.filter(item => 
      item.energy !== "0MJ" && item.energy !== "N/A"
    );
  },

  // Get items by resource sink points range
  getByResourceSinkPoints: (min, max) => {
    return SatisfactoryItems.filter(item => {
      const points = parseInt(item.resourceSinkPoints);
      return !isNaN(points) && points >= min && points <= max;
    });
  },

  // Sort items by resource sink points
  sortByResourceSinkPoints: (ascending = true) => {
    return [...SatisfactoryItems].sort((a, b) => {
      const aPoints = parseInt(a.resourceSinkPoints) || 0;
      const bPoints = parseInt(b.resourceSinkPoints) || 0;
      return ascending ? aPoints - bPoints : bPoints - aPoints;
    });
  }
};

// Export utilities
if (typeof module !== 'undefined' && module.exports) {
  module.exports.ItemUtils = ItemUtils;
} else if (typeof window !== 'undefined') {
  window.ItemUtils = ItemUtils;
} 